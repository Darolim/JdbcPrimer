package model;

public class Kurs {

	private int IDKursa;
	private String imeKursa;
	private int cenaKursa;
	
	public int getIDKursa() {
		return IDKursa;
	}
	public void setIDKursa(int iDKursa) {
		IDKursa = iDKursa;
	}
	public String getImeKursa() {
		return imeKursa;
	}
	public void setImeKursa(String imeKursa) {
		this.imeKursa = imeKursa;
	}
	public int getCenaKursa() {
		return cenaKursa;
	}
	public void setCenaKursa(int cenaKursa) {
		this.cenaKursa = cenaKursa;
	}
		
}
